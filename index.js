function add (a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

function inc(a) {
  let increment = a;
  return increment++;
}

function dec(a) {
  let decrement = a;
  return --a;
}

function makeInt(n) {
  return parseInt(n, 10);
}

